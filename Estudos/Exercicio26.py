# Criando uma variável com o valor inicial 0
valor = int(input('Digite um número: '))
# nesse caso aqui a váriavel (Valor), irá receber um número que será somado mais 1, até chegar no número 10 
while valor <= 10:
    print(valor)
    valor= valor + 1
    
"""toda vez que se faz necessário repetir uma determinada intrução enquanto um determinado objetivo não é atingido
,podemos realizar essa repetição por meio da estrutura de repetição while"""